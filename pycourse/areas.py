# rectangle
rect_length = 10
rect_height = 20

rect_area = rect_length * rect_height  # calculate the area of rectangle
print(f'The area of a rectangle of length {rect_length} and height {rect_height} is {rect_area}')

# circle
circle_radius = 10
PI = 3.14159265358

circle_area = (circle_radius ** 2) * PI  # calculate the area of circle
print(f'The area of a circle of radius {circle_radius} is {circle_area}')
